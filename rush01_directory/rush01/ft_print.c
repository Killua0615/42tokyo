#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print(char *c)
{
	while (*c != '\0')
	{
		ft_putchar(*c);
		c++;
	}
	write(1, "\n", 1);
}